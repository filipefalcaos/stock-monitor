<!-- Template -->
<template>
  <section class="section" :class="process.platform === 'win32' ? 'section-win' : 'section'">
    <div class="columns is-vcentered">
      <div class="column is-2">
        <b-select
          @input="load_portfolio"
          v-model="portfolio_data.last_portfolio"
          placeholder="Selecione uma carteira"
          expanded
        >
          <option
            v-for="option in portfolio_data.portfolios"
            :value="option.id"
            :key="option.id"
          >{{ option.name }}</option>
        </b-select>
      </div>

      <div class="column is-4">
        <div class="buttons">
          <b-button @click="add_portfolio_dialog" type="is-success">Adicionar Carteira</b-button>
          <b-button @click="get_stock_prices" :loading="is_processing" type="is-info">Atualizar</b-button>
        </div>
      </div>

      <div class="column">
        <h1
          class="title"
          :class="process.platform === 'win32' ? 'base-text-win' : 'base-text'"
          style="float: right;"
        >
          <b>Valor Feito:</b>
          <span style="margin-right: 0.5rem;">{{ format_num(full_value) }}</span>
          <span>|</span>

          <b style="margin-left: 0.5rem;">Valor Ativo:</b>
          <span style="margin-right: 0.5rem;">{{ format_num(active_value) }}</span>
          <span>|</span>

          <b
            v-if="has_error"
            style="margin-left: 0.5rem; text-align: justify; "
          >Resultados indispon√≠veis</b>
          <span v-else>
            <b style="margin-left: 0.5rem;">Resultado:</b>
            {{ format_num(final_result) }} ({{ percent_result.toFixed(2) }}%)

            <span v-if="final_result > 0">üòÄ</span>
            <span v-else-if="final_result == 0">üòê</span>
            <span v-else-if="final_result < 0">üò¢</span>
          </span>
        </h1>
      </div>
    </div>

    <div class="columns is-vcentered" style="margin-top: 1rem;">
      <div class="column is-10">
        <h1
          class="title"
          :class="process.platform === 'win32' ? 'title-text-win' : 'title-text'"
        >Posi√ß√£o: Comprado</h1>
      </div>

      <div class="column is-2">
        <b-button
          @click="add_stock_dialog(true)"
          style="float: right;"
          type="is-info"
          icon-left="plus"
          outlined
        >A√ß√£o</b-button>
      </div>
    </div>

    <div class="columns">
      <div class="column is-full">
        <stock-table :stock-data="stock_data_buy" :new-data="new_data"></stock-table>
      </div>
    </div>

    <div class="columns is-vcentered" style="margin-top: 1rem;">
      <div class="column is-10">
        <h1
          class="title title-text"
          :class="process.platform === 'win32' ? 'title-text-win' : 'title-text'"
        >Posi√ß√£o: Vendido</h1>
      </div>

      <div class="column is-2">
        <b-button
          @click="add_stock_dialog(false)"
          style="float: right;"
          type="is-info"
          icon-left="plus"
          outlined
        >A√ß√£o</b-button>
      </div>
    </div>

    <div class="columns">
      <div class="column is-full">
        <stock-table :stock-data="stock_data_sell" :new-data="new_data"></stock-table>
      </div>
    </div>

    <b-modal
      :active.sync="is_processing_stock"
      has-modal-card
      trap-focus
      aria-role="dialog"
      aria-modal
    >
      <stock-form v-on:submit-stock="add_stock" :stocks="available_stocks" :is-buying="is_buying"/>
    </b-modal>
  </section>
</template>

<!-- Script -->
<script>
import path from "path";
import { remote } from "electron";
const fs = require("fs");

import StockTable from "./StockTable";
import StockForm from "./StockForm";

export default {
  name: "main-page",
  components: { StockTable, StockForm },

  // Gets the last state of the portfolio data. Also, gets the current state
  // of the stocks when the component is created
  created() {
    this.portfolio_data = JSON.parse(
      fs.readFileSync(__static + "/portfolio-data.json")
    );

    this.fileName = path.join(remote.app.getPath('userData'), '/portfolio-data.json');
    console.log("Config path: " + this.fileName);

    /* Loads the portfolio data file */
    try {
      if (fs.existsSync(this.fileName)) {
        this.portfolio_data = JSON.parse(fs.readFileSync(this.fileName));
        console.log("Loaded config file");
      } else {
        this.portfolio_data = JSON.parse(fs.readFileSync(__static + "/portfolio-data.json"));
        fs.writeFileSync(this.fileName, JSON.stringify(this.portfolio_data));
        console.log("New config file created");
      }
    } catch (error) {
      console.error(error);
    }

    // Starts the UI state
    this.get_portfolios_data();
    this.get_stock_prices();
    this.get_available_stocks();
  },

  data() {
    return {
      process: process,
      new_data: false,
      is_processing: false,
      is_processing_stock: false,
      is_buying: true,
      has_error: false,
      full_value: 0,
      active_value: 0,
      final_result: 0,
      percent_result: 0,
      portfolio_data: {},
      stock_data: [],
      available_stocks: [],
      fileName: ""
    };
  },

  computed: {
    stock_data_buy() {
      return this.stock_data.filter(stock => {
        return stock.buy;
      });
    },

    stock_data_sell() {
      return this.stock_data.filter(stock => {
        return !stock.buy;
      });
    }
  },

  methods: {
    format_num(num) {
      let number = parseFloat(num).toFixed(2);
      return new Intl.NumberFormat("pt-BR", {
        style: "currency",
        currency: "BRL"
      }).format(number);
    },

    update_selected_data(portfolio) {
      this.stock_data = portfolio.stocks;
      this.full_value = portfolio.investment;
      this.active_value = this.full_value;

      portfolio.stocks.forEach(stock => {
        if (stock.sold) {
          this.active_value -= stock.sell_price * stock.amount;
        }
      });
    },

    get_portfolios_data() {
      let lastPortfolio = this.portfolio_data.portfolios.find(portfolio => {
        return portfolio.id == this.portfolio_data.last_portfolio;
      });

      if (lastPortfolio) this.update_selected_data(lastPortfolio);
    },

    load_portfolio() {
      let newPotfolio = this.portfolio_data.portfolios.find(portfolio => {
        return portfolio.id == this.portfolio_data.last_portfolio;
      });

      // Updates the portfolio data file and the stocks data
      fs.writeFileSync(this.fileName, JSON.stringify(this.portfolio_data));
      this.update_selected_data(newPotfolio);
      this.get_stock_prices();
    },

    create_portfolio(portfolio_name) {
      this.portfolio_data.id_count++;
      let newPortfolio = {
        name: portfolio_name,
        id: this.portfolio_data.id_count,
        investment: 0,
        stocks: []
      };

      // Adds an empty portfolio with the given name
      this.portfolio_data.portfolios.push(newPortfolio);
      this.selected_portfolio = this.portfolio_data.last_portfolio = this.portfolio_data.id_count;
      this.final_result = 0;
      this.percent_result = 0;

      // Updates the portfolio data file and the UI
      fs.writeFileSync(this.fileName, JSON.stringify(this.portfolio_data));
      this.update_selected_data(newPortfolio);
    },

    add_portfolio_dialog() {
      this.$buefy.dialog.prompt({
        message: "Insira um nome para a nova carteira.",
        inputAttrs: {
          placeholder: "Portf√≥lio",
          maxlength: 30
        },
        confirmText: "Adicionar",
        cancelText: "Cancelar",
        trapFocus: true,
        type: "is-info",
        onConfirm: value => this.create_portfolio(value)
      });
    },

    add_stock(new_stock) {
      let lastPortfolio = this.portfolio_data.portfolios.find(portfolio => {
        return portfolio.id == this.portfolio_data.last_portfolio;
      });

      let newStock = {
        stock: new_stock.stock.code,
        uol_code: new_stock.stock.idt,
        first_price: new_stock.first_price,
        amount: new_stock.amount,
        buy: new_stock.buy,
        sold: false
      };

      // Updates the portfolio data file and the UI
      lastPortfolio.stocks.push(newStock);
      fs.writeFileSync(this.fileName, JSON.stringify(this.portfolio_data));
      this.update_selected_data(lastPortfolio);
      this.get_stock_prices();
    },

    add_stock_dialog(is_buying) {
      this.is_processing_stock = true;
      this.is_buying = is_buying;
    },

    get_stock_prices() {
      let promises = [];
      let base_url = "http://cotacoes.economia.uol.com.br/ws/asset/";
      this.is_processing = true;
      this.has_error = false;

      // Gets the most recent price of each stock
      this.stock_data.forEach(stock => {
        promises.push(
          this.$http
            .get(base_url + stock.uol_code + "/intraday?size=1")
            .then(response => {
              stock.aux_price = response.data.data[0].price;
              stock.aux_var = response.data.data[0].var;
              stock.aux_varpct = response.data.data[0].varpct;
            })
        );
      });

      // After all prices are collected, update the UI
      Promise.all(promises)
        .then(() => {
          this.stock_data.forEach(stock => {
            if (stock.sold) {
              stock.current_price = stock.sell_price;
            } else {
              stock.current_price = stock.aux_price;
            }

            stock.var = stock.aux_var;
            stock.varpct = stock.aux_varpct;
          });

          this.update_stock_prices();
          this.new_data = true;
          this.is_processing = false;
          setTimeout(() => {
            this.new_data = false;
          }, 2000);
        })
        .catch(error => {
          this.has_error = true;
          if (this.is_processing) this.is_processing = false;

          // Display an error message
          this.$buefy.notification.open({
            duration: 5000,
            message:
              "Falha ao se conectar ao servidor. Por favor, cheque sua conex√£o.",
            position: "is-bottom-right",
            type: "is-danger"
          });
        });
    },

    update_stock_prices() {
      let sum = 0;

      this.stock_data.forEach(stock => {
        if (stock.buy) {
          stock.result = (
            (stock.current_price - stock.first_price) *
            stock.amount
          ).toFixed(2);
        } else {
          stock.result = (
            (stock.first_price - stock.current_price) *
            stock.amount
          ).toFixed(2);
        }

        sum += parseFloat(stock.result);
      });

      this.final_result = parseFloat(sum).toFixed(2);
      if (this.full_value === 0) {
        this.percent_result = 0;
      } else {
        this.percent_result = (sum / this.full_value) * 100;
      }
    },

    get_available_stocks() {
      let base_url = "http://cotacoes.economia.uol.com.br/ws/asset/";
      this.$http
        .get(base_url + "stock/list?size=10000")
        .then(response => {
          this.available_stocks = response.data.data;
        })
        .catch(error => {
          this.$buefy.notification.open({
            duration: 5000,
            message:
              "N√£o foi poss√≠vel obter a lista de a√ß√µes. Por favor, cheque sua conex√£o.",
            position: "is-bottom-right",
            type: "is-danger"
          });
        });
    }
  }
};
</script>
